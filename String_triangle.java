
/*
 * a
 * bb
 * cccc
 * ddddd
 * eeeeee
 */
public class String_triangle
{
    public static void main()
    {
      char i,j;
      for(i='a';i<='e';i++)
      {
          for(j='a';j<=i;j++)
          {
              System.out.print(j);
            }
            System.out.println(" ");
        }
    }
}

      
        